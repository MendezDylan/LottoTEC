    public void AgregarFechaSorteos(){
        String sorteo=comboSorteos.getSelectedItem().toString();
        
        DoublyLinkedList<String> listaFechas =listaSorteosPendientes.consultAll(listaSorteosPendientes.getHead().getElement());
        DoublyLinkedNode<String> nodoAux=listaFechas.getHead();
        System.out.println(listaFechas.toString());
        while(nodoAux!=null){
            comboFecha.addItem(nodoAux.getElement());
            nodoAux=nodoAux.getNext();
        }
        
    }
    
