    private void botonEscogerNumeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEscogerNumeroActionPerformed
        // TODO add your handling code here:
        if(comboSorteos.getSelectedItem()=="Loteria"){
            new EscogerLoteria(jFrame1, rootPaneCheckingEnabled, numerosEscogidos).setVisible(true);
        }if(comboSorteos.getSelectedItem()=="Lotto"){
            new EscogerLotto(jFrame1, rootPaneCheckingEnabled,numerosEscogidos).setVisible(true);
        }if(comboSorteos.getSelectedItem()=="Bingo"){
            new EscogerBingo(jFrame1, rootPaneCheckingEnabled,numerosEscogidos).setVisible(true);
        }if(comboSorteos.getSelectedItem()=="Tiempos"){
            new EscogerTiempos(jFrame1, rootPaneCheckingEnabled,numerosEscogidos).setVisible(true);
        }
    }//GEN-LAST:event_botonEscogerNumeroActionPerformed

